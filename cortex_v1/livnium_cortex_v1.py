class LivniumGovernedCircuit:
    """
    Full Livnium–MPS stack.

    The entropy ceiling at each bond is dynamically modulated by the
    Livnium semantic polarity of the last lattice rotation:

        Step 1: apply a cube rotation to the lattice
                → extract α = mean |cos θ| for all moved symbols
        Step 2: set governor.alpha = α
                → effective_S_max_i = S_max_base × (1 + α × polarity_i)
        Step 3: build and run a quantum circuit on the MPS

    Parameters
    ----------
    n_qubits : number of qubits in the MPS
    s_max    : base entropy ceiling (nats).  Default: 2 log(2) ≈ 2 ebits.
    max_bond_dim : hard bond dimension cap (default 64)
    verbose  : forward governor pruning messages
    """

    def __init__(
        self,
        n_qubits: int,
        s_max: float = 2.0 * np.log(2),
        max_bond_dim: int = 64,
        verbose: bool = False,
    ):
        self.sim = MPSSimulator(n_qubits=n_qubits, max_bond_dim=max_bond_dim)
        self.gov = SemanticPolarityGovernor(
            self.sim, S_max=s_max, alpha=0.5, verbose=verbose
        )
        self.last_alpha: float = 0.0
        self.rotation_log: List[Dict] = []

    # ── Lattice coupling ───────────────────────────────────────────────────────

    def apply_livnium_rotation(
        self,
        lattice: LivniumLattice,
        rotation_fn: Callable,
    ) -> float:
        """
        Apply rotation_fn to lattice, update governor α, and log the event.

        Returns
        -------
        float : mean |cos θ| = α for this rotation
        """
        result = lattice.apply_rotation_with_polarity(rotation_fn)
        alpha  = result["mean_abs"]

        self.last_alpha = alpha
        self.gov.alpha  = alpha

        ok, msg = lattice.is_valid()
        self.rotation_log.append(dict(
            alpha=alpha,
            n_moved=result["n_moved"],
            pol_min=result["min"],
            pol_max=result["max"],
            lattice_valid=ok,
            lattice_msg=msg,
        ))
        return alpha

    # ── Quantum gates (forwarded to governor) ─────────────────────────────────

    def hadamard(self, q: int):           self.gov.hadamard(q)
    def cnot(self, ctrl: int, tgt: int):  self.gov.cnot(ctrl, tgt)
    def rz(self, q: int, theta: float):   self.gov.rz(q, theta)
    def rx(self, q: int, theta: float):   self.gov.rx(q, theta)
    def ry(self, q: int, theta: float):   self.gov.ry(q, theta)
    def apply_gate(self, q: int, U: np.ndarray): self.gov.apply_gate(q, U)

    def measure_all(self) -> List[int]:
        return self.sim.measure_all()

    # ── Diagnostics ───────────────────────────────────────────────────────────

    def summary(self, verbose: bool = True) -> Dict:
        """Return (and optionally print) a summary of the circuit state."""
        data = dict(
            n_qubits       = self.sim.n,
            rotation_events= len(self.rotation_log),
            governor_prunes= len(self.gov.pruning_log),
            mean_alpha     = float(np.mean([e["alpha"] for e in self.rotation_log]))
                             if self.rotation_log else 0.0,
            max_bond_used  = self.sim.max_bond_dim_used,
            memory_bytes   = self.sim.memory_bytes,
            polarity       = self.gov.polarity_summary(),
        )
        if verbose:
            print(f"\n{'─'*55}")
            print(f"  LivniumGovernedCircuit  (n={data['n_qubits']})")
            print(f"  Rotation events : {data['rotation_events']}")
            print(f"  Governor prunes : {data['governor_prunes']}")
            print(f"  Mean α          : {data['mean_alpha']:.4f}")
            print(f"  Max χ used      : {data['max_bond_used']}")
            print(f"  Memory          : {data['memory_bytes']:,} bytes")
            print(f"{'─'*55}")
        return data


