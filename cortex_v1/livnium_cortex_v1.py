# §3  ENTANGLEMENT GOVERNOR  (α-unaware baseline)
# ═══════════════════════════════════════════════════════════════════════════════

class EntanglementGovernor:
    """
    Wraps MPSSimulator and enforces a hard Von Neumann entropy ceiling at
    every bond after every gate.

    Parameters
    ----------
    sim     : MPSSimulator to govern
    S_max   : entropy ceiling in nats  (log(2) ≈ 1 ebit)
    verbose : print every pruning event
    """

    def __init__(
        self,
        sim: MPSSimulator,
        S_max: float = np.log(64),
        verbose: bool = False,
    ):
        self.sim     = sim
        self.S_max   = S_max
        self.verbose = verbose
        self.pruning_log: List[Dict]        = []
        self.entropy_history: List[List[float]] = []

    # ── Core: enforce one bond ─────────────────────────────────────────────────

    def _enforce_bond(self, site: int) -> bool:
        t = self.sim.tensors[site]
        chi_l, _, chi_r = t.shape
        U, s, Vh = np.linalg.svd(t.reshape(chi_l * 2, chi_r), full_matrices=False)
        S_current = von_neumann_entropy(s)
        if S_current <= self.S_max:
            return False

        lam2 = s ** 2
        norm = lam2.sum()
        if norm < 1e-15:
            return False
        p = lam2 / norm

        cumS, chi_keep = 0.0, 1
        for k, pk in enumerate(p):
            if pk > 1e-15: cumS -= pk * np.log(pk)
            if cumS >= self.S_max and k > 0:
                chi_keep = k
                break
        else:
            chi_keep = len(s)
        chi_keep = max(1, chi_keep)
        if chi_keep >= len(s):
            return False

        trunc_err = float(np.sum(s[chi_keep:] ** 2))
        US = (U[:, :chi_keep] * s[:chi_keep])
        self.sim.tensors[site]     = US.reshape(chi_l, 2, chi_keep)
        self.sim.tensors[site + 1] = (
            Vh[:chi_keep, :] @ self.sim.tensors[site + 1].reshape(chi_r, -1)
        ).reshape(chi_keep, 2, self.sim.tensors[site + 1].shape[2])

        event = dict(bond=site, chi_before=len(s), chi_after=chi_keep,
                     S_before=S_current, S_after=von_neumann_entropy(s[:chi_keep]),
                     trunc_err=trunc_err)
        self.pruning_log.append(event)
        if self.verbose:
            print(f"  [PRUNE] bond {site}: χ {len(s)}→{chi_keep} "
                  f"S {S_current:.3f}→{event['S_after']:.3f} err={trunc_err:.2e}")
        return True

    def enforce_all(self):
        for i in range(self.sim.n - 1):
            self._enforce_bond(i)

    # ── Gate wrappers ─────────────────────────────────────────────────────────

    def hadamard(self, site: int):
        self.sim.hadamard(site); self.enforce_all(); self._snapshot()

    def cnot(self, control: int, target: int):
        self.sim.cnot(control, target); self.enforce_all(); self._snapshot()

    def rz(self, site: int, theta: float):
        self.sim.rz(site, theta); self._snapshot()

    def rx(self, site: int, theta: float):
        self.sim.rx(site, theta); self._snapshot()

    def ry(self, site: int, theta: float):
        self.sim.ry(site, theta); self._snapshot()

    def apply_gate(self, site: int, U: np.ndarray):
        self.sim.apply_gate(site, U); self.enforce_all(); self._snapshot()

    def measure_all(self) -> List[int]:
        return self.sim.measure_all()

    # ── Monitoring ────────────────────────────────────────────────────────────

    def _snapshot(self):
        self.entropy_history.append(bond_entropies(self.sim))

    def current_entropies(self) -> List[float]:
        return bond_entropies(self.sim)

    def max_entropy(self) -> float:
        e = self.current_entropies()
        return max(e) if e else 0.0

    def total_entanglement(self) -> float:
        return sum(self.current_entropies())


