Coord = Tuple[int, int, int]

_COORDS_27: List[Coord] = [
    (x, y, z)
    for x in range(-1, 2)
    for y in range(-1, 2)
    for z in range(-1, 2)
]


def boundary_exposure(coord: Coord) -> int:
    """f = number of coordinates equal to ±1."""
    return sum(1 for c in coord if abs(c) == 1)


def symbolic_weight(coord: Coord) -> int:
    """SW = 9f   (spec §1 A4)"""
    return 9 * boundary_exposure(coord)


class LivniumLattice:
    """
    3×3×3 Livnium lattice.

    State: dict[coord → symbol_id]  (27 symbols, ids 0–26)
    Initial: symbol i lives at _COORDS_27[i]

    Only the 24 cube rotations are permitted.

    Verified invariants:
        R⁴ = I for all generators
        ΣSW = 486 invariant under all 24 rotations
        Class counts: {0:1, 1:6, 2:12, 3:8} preserved
        Bijection (each symbol appears exactly once) preserved
    """

    def __init__(self):
        self.state: Dict[Coord, int] = {c: i for i, c in enumerate(_COORDS_27)}

    def copy(self) -> "LivniumLattice":
        lat = LivniumLattice.__new__(LivniumLattice)
        lat.state = dict(self.state)
        return lat

    # ── Rotation primitives (all three generators, verified) ─────────────────

    def _apply_rotation(self, fn: Callable):
        self.state = {fn(x, y, z): sym for (x, y, z), sym in self.state.items()}

    def rotate_z90(self):
        """Rz(90°): (x,y,z) → (−y, x, z)"""
        self._apply_rotation(lambda x, y, z: (-y, x, z))

    def rotate_x90(self):
        """Rx(90°): (x,y,z) → (x, −z, y)"""
        self._apply_rotation(lambda x, y, z: (x, -z, y))

    def rotate_y90(self):
        """Ry(90°): (x,y,z) → (z, y, −x)"""
        self._apply_rotation(lambda x, y, z: (z, y, -x))

    # ── Conservation checks ────────────────────────────────────────────────────

    def total_sw(self) -> int:
        """ΣSW — must equal 486 for 3×3×3."""
        return sum(symbolic_weight(c) for c in self.state)

    def class_counts(self) -> Dict[int, int]:
        """Returns {f: count} — must equal {0:1, 1:6, 2:12, 3:8}."""
        counts: Dict[int, int] = {0: 0, 1: 0, 2: 0, 3: 0}
        for c in self.state:
            counts[boundary_exposure(c)] += 1
        return counts

    def is_bijection(self) -> bool:
        syms = list(self.state.values())
        return len(set(syms)) == len(syms) == 27

    def is_valid(self) -> Tuple[bool, str]:
        """Return (True, 'OK') or (False, reason) — spec §2 D5."""
        if self.total_sw() != 486:
            return False, f"ΣSW={self.total_sw()} ≠ 486"
        cc = self.class_counts()
        if cc != {0: 1, 1: 6, 2: 12, 3: 8}:
            return False, f"class counts {cc}"
        if not self.is_bijection():
            return False, "bijection violated"
        return True, "OK"

    # ── Semantic polarity ──────────────────────────────────────────────────────

    def semantic_polarity(self, old_coord: Coord, new_coord: Coord) -> float:
        """
        cos(θ) between the symbol's motion vector and its observer direction.

        motion_vector = new − old
        observer_dir  = origin − old  (toward Global Observer at (0,0,0))

        Returns 0.0 if the symbol doesn't move or sits at the origin.
        """
        m = np.array(new_coord, float) - np.array(old_coord, float)
        o = -np.array(old_coord, float)
        m_n, o_n = np.linalg.norm(m), np.linalg.norm(o)
        if m_n < 1e-12 or o_n < 1e-12:
            return 0.0
        return float(np.dot(m, o) / (m_n * o_n))

    def apply_rotation_with_polarity(self, rotation_fn: Callable) -> Dict:
        """
        Apply rotation_fn(self), then compute semantic polarity for every
        symbol that moved.

        rotation_fn must accept the lattice as its first (and only) argument:
            lambda l: l.rotate_z90()

        Returns:
            polarities : list of cos(θ) per moved symbol
            mean_abs   : mean |cos θ|  ← the α bridge signal
            min, max   : polarity range
            n_moved    : symbols that changed position
        """
        old_pos = {sym: coord for coord, sym in self.state.items()}
        rotation_fn(self)
        new_pos = {sym: coord for coord, sym in self.state.items()}

        pols = [
            self.semantic_polarity(old_pos[s], new_pos[s])
            for s in range(27)
            if old_pos[s] != new_pos[s]
        ]
        mean_abs = float(np.mean(np.abs(pols))) if pols else 0.0
        return dict(
            polarities=pols,
            mean_abs=mean_abs,
            min=float(min(pols)) if pols else 0.0,
            max=float(max(pols)) if pols else 0.0,
            n_moved=len(pols),
        )


# ─────────────────────────────────────────────────────────────────────────────
# 24-rotation generator (BFS over three generators)
# ─────────────────────────────────────────────────────────────────────────────

def generate_all_24_rotations() -> List[Tuple[str, Callable]]:
    """
    Return all 24 distinct cube orientations as (label, fn) pairs where
    fn accepts a LivniumLattice as its single argument.

    Example:
        rots = generate_all_24_rotations()
        label, fn = rots[7]
        fn(lattice)   # applies the rotation in-place
    """
    def _key(lat: LivniumLattice) -> tuple:
        return tuple(sorted(lat.state.items()))

    def _make_fn(seq: List[str]) -> Callable:
        def _fn(l: LivniumLattice):
            for axis in seq:
                if axis == "X":    l.rotate_x90()
                elif axis == "Y":  l.rotate_y90()
                elif axis == "Z":  l.rotate_z90()
        return _fn

    gen_fns = {
        "X": lambda l: l.rotate_x90(),
        "Y": lambda l: l.rotate_y90(),
        "Z": lambda l: l.rotate_z90(),
    }

    seen, rotations = set(), []
    queue = deque([([], LivniumLattice())])

    while queue:
        seq, lat = queue.popleft()
        k = _key(lat)
        if k in seen:
            continue
        seen.add(k)
        label = "".join(seq) if seq else "I"
        rotations.append((label, _make_fn(seq)))
        if len(rotations) == 24:
            break
        for axis, gen in gen_fns.items():
            nxt = lat.copy()
            gen(nxt)
            queue.append((seq + [axis], nxt))

    return rotations


# ─────────────────────────────────────────────────────────────────────────────
# Bridge: single α signal from a rotation
# ─────────────────────────────────────────────────────────────────────────────

def livnium_polarity_signal(lattice: LivniumLattice, rotation_fn: Callable) -> float:
    """
    Apply rotation_fn(lattice) and return mean |cos θ| as the α bridge signal.

    ≈ 0.595  →  90° quarter-turn  (tangential motion, low structure)
    ≈ 0.722  →  120° vertex rotation
    ≈ 0.836  →  180° half-turn (Pauli family, high structure)
    = 0.000  →  identity
    """
    return lattice.apply_rotation_with_polarity(rotation_fn)["mean_abs"]


