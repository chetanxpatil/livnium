# §2  ENTROPY UTILITIES
# ═══════════════════════════════════════════════════════════════════════════════

def von_neumann_entropy(singular_values: np.ndarray) -> float:
    """
    S = −Σ p_k log p_k   where   p_k = λ_k² / Σλ²

    singular_values : raw (unnormalised) values from SVD.
    Returns entropy in nats.  Use log2 for bits.
    """
    lam2 = singular_values ** 2
    norm = lam2.sum()
    if norm < 1e-15:
        return 0.0
    p = lam2 / norm
    p = p[p > 1e-15]
    return float(-np.sum(p * np.log(p)))


def bond_entropies(sim: MPSSimulator) -> List[float]:
    """Von Neumann entropy at every bond of an MPS."""
    result = []
    for i in range(sim.n - 1):
        t = sim.tensors[i]
        chi_l = t.shape[0]
        _, s, _ = np.linalg.svd(t.reshape(chi_l * 2, t.shape[2]), full_matrices=False)
        result.append(von_neumann_entropy(s))
    return result


def polarity_score_theoretical(
    singular_values: np.ndarray,
    bond_site: int,
    n_qubits: int,
) -> float:
    """
    polarity = 1 − S / S_max_theoretical

    S_max_theoretical(i) = min(i+1, n−i−1) × log(2)
        = Page limit / bipartition maximum for an n-qubit system at bond i.

    GHZ bonds (Area Law): S = log(2) << S_max_theo  →  polarity ≈ 0.9
    Random deep circuit:  S → S_max_theo             →  polarity ≈ 0.0
    """
    n_left  = bond_site + 1
    n_right = n_qubits - n_left
    S_max   = min(n_left, n_right) * np.log(2)
    if S_max < 1e-15:
        return 1.0
    return float(np.clip(1.0 - von_neumann_entropy(singular_values) / S_max, 0.0, 1.0))


def bond_polarities(sim: MPSSimulator) -> List[float]:
    """Polarity at every bond of an MPS."""
    result = []
    for i in range(sim.n - 1):
        t = sim.tensors[i]
        chi_l = t.shape[0]
        _, s, _ = np.linalg.svd(t.reshape(chi_l * 2, t.shape[2]), full_matrices=False)
        result.append(polarity_score_theoretical(s, i, sim.n))
    return result


