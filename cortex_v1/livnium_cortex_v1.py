# §4  SEMANTIC POLARITY GOVERNOR  (primary)
# ═══════════════════════════════════════════════════════════════════════════════

class SemanticPolarityGovernor(EntanglementGovernor):
    """
    Polarity-aware entanglement governor.

    Effective ceiling per bond:
        polarity_i = 1 − S_i / (min(i+1, n−i−1) × log 2)
        effective_S_max_i = S_max_base × (1 + α × polarity_i)

    High polarity (Area Law bond, structured state) → looser ceiling → bond survives.
    Low polarity  (Volume Law bond, noise)          → standard ceiling → bond pruned.

    α = 0.0  → identical to EntanglementGovernor (no polarity adjustment)
    α = 0.5  → Area Law bonds get 50 % headroom above base ceiling
    α = 1.0  → Area Law bonds get double the base ceiling

    Parameters
    ----------
    sim     : MPSSimulator to govern
    S_max   : base entropy ceiling (nats)
    alpha   : polarity reward strength ∈ [0, ∞)
    verbose : print every pruning event
    """

    def __init__(
        self,
        sim: MPSSimulator,
        S_max: float = np.log(64),
        alpha: float = 0.5,
        verbose: bool = False,
    ):
        super().__init__(sim, S_max=S_max, verbose=verbose)
        self.alpha = alpha
        self.polarity_log: List[Dict] = []

    def _enforce_bond(self, site: int) -> bool:
        t = self.sim.tensors[site]
        chi_l, _, chi_r = t.shape
        U, s, Vh = np.linalg.svd(t.reshape(chi_l * 2, chi_r), full_matrices=False)
        S_current = von_neumann_entropy(s)

        p_score       = polarity_score_theoretical(s, site, self.sim.n)
        eff_S_max     = self.S_max * (1.0 + self.alpha * p_score)

        self.polarity_log.append(dict(
            bond=site, polarity=p_score,
            S_current=S_current, effective_S_max=eff_S_max,
        ))

        if S_current <= eff_S_max:
            return False

        lam2 = s ** 2
        norm = lam2.sum()
        if norm < 1e-15:
            return False
        p = lam2 / norm

        cumS, chi_keep = 0.0, 1
        for k, pk in enumerate(p):
            if pk > 1e-15: cumS -= pk * np.log(pk)
            if cumS >= eff_S_max and k > 0:
                chi_keep = k
                break
        else:
            chi_keep = len(s)
        chi_keep = max(1, chi_keep)
        if chi_keep >= len(s):
            return False

        trunc_err = float(np.sum(s[chi_keep:] ** 2))
        US = (U[:, :chi_keep] * s[:chi_keep])
        self.sim.tensors[site]     = US.reshape(chi_l, 2, chi_keep)
        self.sim.tensors[site + 1] = (
            Vh[:chi_keep, :] @ self.sim.tensors[site + 1].reshape(chi_r, -1)
        ).reshape(chi_keep, 2, self.sim.tensors[site + 1].shape[2])

        event = dict(
            bond=site, polarity=p_score, effective_S_max=eff_S_max,
            chi_before=len(s), chi_after=chi_keep,
            S_before=S_current,
            S_after=von_neumann_entropy(s[:chi_keep]),
            trunc_err=trunc_err,
        )
        self.pruning_log.append(event)
        if self.verbose:
            print(f"  [PRUNE] bond {site}: pol={p_score:.2f} "
                  f"eff_ceil={eff_S_max:.3f} χ {len(s)}→{chi_keep} "
                  f"S {S_current:.3f}→{event['S_after']:.3f} err={trunc_err:.2e}")
        return True

    def polarity_summary(self) -> Dict:
        pols = bond_polarities(self.sim)
        return dict(
            mean_polarity=float(np.mean(pols)),
            max_polarity=float(max(pols)),
            min_polarity=float(min(pols)),
            prune_events=len(self.pruning_log),
            alpha=self.alpha,
            S_max_base=self.S_max,
        )


