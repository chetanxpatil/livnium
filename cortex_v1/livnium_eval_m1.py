class AlphaOnlyExtractor:
    """
    Ablation baseline: rank by |sin(θ/2)| alone, no MPS circuit.
    Isolates whether the circuit governor adds value beyond the
    semantic bridge alone.
    Accepts an optional pre-built projector to avoid double GloVe loads.
    """
    def __init__(self, proj: "SemanticProjector | None" = None):
        if not _SEM_BRIDGE:
            raise RuntimeError("semantic_bridge not available.")
        if proj is not None:
            self.proj = proj
            print("[Alpha-Only] Reusing shared SemanticProjector.", flush=True)
        else:
            print("[Alpha-Only] Initialising SemanticProjector …", flush=True)
            self.proj = SemanticProjector()
            print("[Alpha-Only] Ready.", flush=True)

    def extract(self, text: str, k: int) -> list[str]:
        tokens = tokenise(text)
        scored = []
        for word in tokens:
            result = self.proj.word_to_axis_angle(word, angle_mode="cosine")
            if result is not None:
                _, theta = result
                alpha = abs(np.sin(theta / 2))
            else:
                alpha = 0.5   # unknown word — neutral score
            scored.append((word, alpha))
        scored.sort(key=lambda x: x[1], reverse=True)
        # Deduplicate while preserving order
        seen = set()
        out = []
        for w, _ in scored:
            if w not in seen:
                seen.add(w)
                out.append(w)
            if len(out) == k:
                break
        return out


