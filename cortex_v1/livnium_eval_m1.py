def load_reuters(n_docs: int, seed: int = 42):
    """
    Load Reuters-21578 news articles with NLTK.
    Gold keywords = category labels (e.g. 'grain', 'trade', 'earn').
    These are expert-assigned domain keywords — clean single-word gold standard.
    Returns list of {'abstract': str, 'keywords': [str]} dicts.
    """
    import nltk
    nltk.download("reuters", quiet=True)
    from nltk.corpus import reuters

    # Use 'test' split documents only (avoids train contamination)
    test_ids = [fid for fid in reuters.fileids() if fid.startswith("test/")]
    rng = np.random.default_rng(seed)
    rng.shuffle(test_ids)

    docs = []
    for fid in test_ids:
        cats = reuters.categories(fid)
        text = reuters.raw(fid).strip()
        # Require non-trivial text and at least one category
        if len(text.split()) >= 30 and cats:
            docs.append({"abstract": text, "keywords": cats})
        if len(docs) >= n_docs:
            break

    print(f"[dataset] Loaded {len(docs)} Reuters test documents "
          f"({len(test_ids)} available).")
    return docs


def load_inspec(n_docs: int, seed: int = 42):
    """
    Try to load Inspec; fall back to Reuters-21578 if unavailable.
    Reuters is the preferred fallback: ~3,700 documents, expert category labels.
    """
    try:
        from datasets import load_dataset
        print("[dataset] Trying Inspec from HuggingFace …", flush=True)
        ds = load_dataset("midas/inspec", "raw", split="test")
        rng = np.random.default_rng(seed)
        indices = rng.choice(len(ds), size=min(n_docs, len(ds)), replace=False)
        docs = []
        for i in indices:
            row = ds[int(i)]
            abstract = row.get("abstract", "") or ""
            if isinstance(abstract, list):
                abstract = " ".join(abstract)
            kp_raw = row.get("keyphrases", []) or []
            keyphrases = []
            for kp in kp_raw:
                if isinstance(kp, list):
                    keyphrases.extend(kp)
                else:
                    keyphrases.append(str(kp))
            if abstract.strip() and keyphrases:
                docs.append({"abstract": abstract.strip(), "keywords": keyphrases})
        if docs:
            print(f"[dataset] Loaded {len(docs)} documents from Inspec.")
            return docs
        raise ValueError("No documents loaded from Inspec.")
    except Exception as e:
        print(f"[dataset] Inspec unavailable ({type(e).__name__}). "
              f"Using Reuters-21578.")
        return load_reuters(n_docs, seed)


