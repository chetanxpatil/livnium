_STOP = set("""a an the is are was were be been being have has had do does did
    will would shall should may might must can could of in on at to for with
    by from up about into than through during before after above below between
    i me my we our you your he she his her it its they them their this that
    these those and but or nor so yet both either neither as if although
    because since while although though once until unless whether""".split())

def tokenise(text: str) -> list[str]:
    """Lowercase alphabetic tokens, no stopwords, length ≥ 3."""
    tokens = re.findall(r"[a-z]+", text.lower())
    return [t for t in tokens if len(t) >= 3 and t not in _STOP]

def stem_set(words: list[str]) -> set[str]:
    return {stem(w) for w in words}

def precision_at_k(predicted: list[str], gold: list[str], k: int) -> float:
    """Single-word stemmed precision@k."""
    gold_single = [w for phrase in gold for w in phrase.lower().split()]
    gold_stems  = stem_set(gold_single)
    pred_stems  = [stem(w) for w in predicted[:k]]
    hits = sum(1 for s in pred_stems if s in gold_stems)
    return hits / k if k > 0 else 0.0


