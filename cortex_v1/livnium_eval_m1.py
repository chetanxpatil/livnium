def generate_report(results: dict) -> str:
    lines = []
    lines.append("=" * 70)
    lines.append("  LIVNIUM Milestone 1 — Baseline Evaluation Report")
    lines.append(f"  Date: 2026-03-16   Corpus: {results['n_docs']} documents")
    lines.append(f"  Elapsed: {results['elapsed_s']}s")
    lines.append("=" * 70)
    lines.append("")

    # ── Performance table ──────────────────────────────────────────────
    agg = results["aggregate"]
    k_values = results["k_values"]
    names = list(agg.keys())

    header = f"{'Extractor':<18}" + "".join(
        f"  P@{k} (mean±std)    " for k in k_values
    )
    lines.append(header)
    lines.append("-" * len(header))

    for name in names:
        row = f"{name:<18}"
        for k in k_values:
            m = agg[name][k]["mean"]
            s = agg[name][k]["std"]
            row += f"  {m:.4f} ± {s:.4f}      "
        lines.append(row)

    lines.append("")
    lines.append("Statistical Tests (LIVNIUM-B vs baseline, paired two-sided t-test):")
    lines.append("-" * 70)

    ttests = results.get("ttests", {})
    if not ttests:
        lines.append("  LIVNIUM-B not available — no t-tests computed.")
    else:
        for k in k_values:
            lines.append(f"  P@{k}:")
            for name, t in ttests.get(k, {}).items():
                direction = "WINS" if t["livnium_wins"] else "LOSES"
                sig = "(*)" if t["significant"] else "(ns)"
                lines.append(
                    f"    LIVNIUM-B vs {name:<12}: t={t['t']:+.3f}, "
                    f"p={t['p']:.4f}  {sig}  LIVNIUM {direction}"
                )

    lines.append("")
    lines.append("Pass Criterion: LIVNIUM P@10 > TF-IDF P@10, p < 0.05")
    if "LIVNIUM-B" in agg and "TF-IDF" in agg and 10 in k_values:
        lm = agg["LIVNIUM-B"][10]["mean"]
        tm = agg["TF-IDF"][10]["mean"]
        p  = ttests.get(10, {}).get("TF-IDF", {}).get("p", 1.0)
        sig = ttests.get(10, {}).get("TF-IDF", {}).get("significant", False)
        outcome = "PASS" if (lm > tm and sig) else "FAIL"
        lines.append(f"  Result: LIVNIUM={lm:.4f}, TF-IDF={tm:.4f}, "
                     f"p={p:.4f}  →  {outcome}")

    lines.append("")
    lines.append("HONEST CAVEATS:")
    lines.append("  · Keyword extraction is a proxy task; LIVNIUM is not a")
    lines.append("    keyword extractor — it is an information-density filter.")
    lines.append("  · Single-word stemmed match underestimates multi-word")
    lines.append("    keyphrase quality (e.g. 'matrix product state' → 'matrix').")
    lines.append("  · Inspec keyphrases are author-assigned; human agreement")
    lines.append("    on single abstracts is typically 60-70% F1.")
    lines.append("  · This evaluation does NOT constitute a published benchmark.")
    lines.append("    Results must be replicated on a held-out test set by an")
    lines.append("    independent reviewer before TRL 4 can be claimed.")
    lines.append("=" * 70)

    return "\n".join(lines)


