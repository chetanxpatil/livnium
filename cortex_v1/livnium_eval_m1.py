class TFIDFExtractor:
    """
    Fit a TF-IDF vocabulary on the evaluation corpus, then extract
    top-K keywords per document by TF-IDF score.
    """
    def __init__(self, corpus_texts: list[str]):
        if not _SKLEARN:
            raise RuntimeError("scikit-learn required for TF-IDF baseline.")
        self.vec = TfidfVectorizer(
            stop_words="english",
            token_pattern=r"(?u)\b[a-z]{3,}\b",
            max_df=0.95,
            min_df=2,
            ngram_range=(1, 1)
        )
        self.vec.fit(corpus_texts)
        self.feature_names = self.vec.get_feature_names_out()

    def extract(self, text: str, k: int) -> list[str]:
        vec = self.vec.transform([text])
        scores = vec.toarray()[0]
        top_idx = np.argsort(scores)[::-1][:k]
        return [self.feature_names[i] for i in top_idx if scores[i] > 0]


