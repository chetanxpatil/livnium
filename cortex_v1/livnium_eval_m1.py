class LIVNIUMExtractor:
    """
    Config B: Semantic Sequential.
    For each document:
      1. Tokenise into words.
      2. Apply semantic SO(3) rotation + polarity-aware CNOT on a 15-qubit lattice.
      3. After processing, read per-bond polarity scores.
      4. Map bond polarity back to the word that contributed most to each bond.
      5. Return top-K words by mean bond polarity.

    This is a faithful re-implementation of SemanticOrganism from
    semantic_organism_test.py adapted for batch evaluation.
    """
    def __init__(self, n_qubits: int = 15, s_max: float = 1.2,
                 max_bond_dim: int = 64):
        if not _LIVNIUM_READY:
            raise RuntimeError("LIVNIUM modules not available.")
        self.n_qubits = n_qubits
        self.s_max = s_max
        self.max_bond_dim = max_bond_dim
        print("[LIVNIUM] Initialising SemanticProjector …", flush=True)
        self.proj = SemanticProjector(seed_vocab=SEED_VOCAB)
        print("[LIVNIUM] Projector ready.", flush=True)

    def _word_to_su2(self, word: str):
        """Returns (axis [3], theta) using semantic bridge."""
        result = self.proj.word_to_axis_angle(word, angle_mode="cosine")
        if result is not None:
            return result
        # Unknown word — deterministic fallback rotation
        rng = np.random.default_rng(abs(hash(word)) % (2**32))
        axis = rng.standard_normal(3)
        axis /= np.linalg.norm(axis) + 1e-12
        return axis, np.pi / 2

    def _alpha(self, theta: float) -> float:
        return float(abs(np.sin(theta / 2)))

    def extract(self, text: str, k: int) -> list[str]:
        tokens = tokenise(text)
        if not tokens:
            return []

        # Fresh circuit per document
        circuit = LivniumGovernedCircuit(
            n_qubits=self.n_qubits,
            s_max=self.s_max,
            max_bond_dim=self.max_bond_dim
        )

        word_bond_scores: dict[str, list[float]] = defaultdict(list)

        for idx, word in enumerate(tokens):
            qubit = idx % self.n_qubits
            next_q = (qubit + 1) % self.n_qubits
            axis, theta = self._word_to_su2(word)
            alpha = self._alpha(theta)

            # Apply Rx gate scaled by alpha
            try:
                circuit.rx(qubit, alpha * np.pi)
                circuit.cnot(qubit, next_q)
            except Exception:
                pass

            # Read polarity of the bond at this qubit position
            try:
                gov = circuit.governor
                if hasattr(gov, 'bond_polarities') and qubit < len(gov.bond_polarities):
                    pol = float(gov.bond_polarities[qubit])
                    word_bond_scores[word].append(pol)
                else:
                    word_bond_scores[word].append(alpha)
            except Exception:
                word_bond_scores[word].append(alpha)

        # Score words by mean bond polarity, break ties by max
        scored = []
        for word, scores in word_bond_scores.items():
            scored.append((word, float(np.mean(scores)), float(np.max(scores))))

        scored.sort(key=lambda x: (x[1], x[2]), reverse=True)
        return [w for w, _, _ in scored[:k]]


