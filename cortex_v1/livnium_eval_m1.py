def run_evaluation(docs: list[dict], k_values: list[int] = (5, 10),
                   enable_yake: bool = False, s_max: float = 1.2) -> dict:
    """
    Run all extractors on all documents.
    Returns results dict with per-document scores and aggregates.
    """
    texts = [d["abstract"] for d in docs]
    gold_all = [d["keywords"] for d in docs]
    n = len(docs)

    # ── Initialise extractors ──────────────────────────────────────────
    extractors = {}

    if _SKLEARN:
        print("[eval] Fitting TF-IDF on corpus …", flush=True)
        extractors["TF-IDF"] = TFIDFExtractor(texts)

    if enable_yake and _YAKE:
        extractors["YAKE"] = YAKEExtractor()

    if _LIVNIUM_READY:
        livnium_ext = LIVNIUMExtractor(s_max=s_max)
        extractors["Alpha-Only"] = AlphaOnlyExtractor(proj=livnium_ext.proj)
        extractors["LIVNIUM-B"]  = livnium_ext
    else:
        print("[eval] LIVNIUM modules not available — skipping LIVNIUM extractors.")

    if not extractors:
        raise RuntimeError("No extractors available. Check dependencies.")

    # ── Per-document scoring ───────────────────────────────────────────
    scores: dict[str, dict[int, list[float]]] = {
        name: {k: [] for k in k_values} for name in extractors
    }
    predictions: dict[str, list[list[str]]] = {name: [] for name in extractors}

    max_k = max(k_values)
    t0 = time.time()

    for i, (doc, gold) in enumerate(zip(docs, gold_all)):
        if (i + 1) % 25 == 0 or i == 0:
            elapsed = time.time() - t0
            print(f"[eval] Document {i+1}/{n}  ({elapsed:.1f}s elapsed)", flush=True)

        for name, extractor in extractors.items():
            try:
                pred = extractor.extract(doc["abstract"], max_k)
            except Exception as e:
                pred = []
                warnings.warn(f"Extractor {name} failed on doc {i}: {e}")

            predictions[name].append(pred)
            for k in k_values:
                p = precision_at_k(pred, gold, k)
                scores[name][k].append(p)

    # ── Aggregate statistics ───────────────────────────────────────────
    from scipy import stats as sp_stats

    agg = {}
    for name in extractors:
        agg[name] = {}
        for k in k_values:
            s = np.array(scores[name][k])
            agg[name][k] = {
                "mean":   float(np.mean(s)),
                "std":    float(np.std(s)),
                "median": float(np.median(s)),
                "n":      int(len(s))
            }

    # ── Paired t-tests: LIVNIUM-B vs others ──────────────────────────
    ttests = {}
    if "LIVNIUM-B" in extractors:
        for k in k_values:
            livnium_scores = np.array(scores["LIVNIUM-B"][k])
            ttests[k] = {}
            for name in extractors:
                if name == "LIVNIUM-B":
                    continue
                other_scores = np.array(scores[name][k])
                t_stat, p_val = sp_stats.ttest_rel(livnium_scores, other_scores)
                ttests[k][name] = {
                    "t":          float(t_stat),
                    "p":          float(p_val),
                    "livnium_wins": bool(float(np.mean(livnium_scores)) >
                                        float(np.mean(other_scores))),
                    "significant":  bool(p_val < 0.05)
                }

    elapsed_total = time.time() - t0

    return {
        "n_docs":    n,
        "k_values":  list(k_values),
        "s_max":     s_max,
        "elapsed_s": round(elapsed_total, 2),
        "aggregate": agg,
        "ttests":    ttests,
        "per_doc_scores": {
            name: {k: scores[name][k] for k in k_values}
            for name in extractors
        }
    }


